
public class IntFloatDouble {

    public static void main(String[] args) {
        
        int varZahl1 = 10;
        float varZahl2 = 10.f;
        double varZahl3 = 10.;
        
        System.out.println("int " + varZahl1);
        System.out.println("float " + varZahl2);
        System.out.println("double " + varZahl3);
        
        varZahl1 = 10 / 3;
        varZahl2 = 10.f / 3;
        varZahl3 = 10.d / 3;
        
        System.out.println("int " + varZahl1);
        System.out.println("float " + varZahl2);
        System.out.println("double " + varZahl3);
        
    }

}
